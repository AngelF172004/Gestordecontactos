    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
          String nombre = jTextField1.getText().trim();
    String telefono = jTextField2.getText().trim();
    String email = jTextField3.getText().trim();
    
    if (nombre.isEmpty() && telefono.isEmpty() && email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debe ingresar al menos un criterio de búsqueda.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    String ruta = "C:\\FICHERO\\" + dato + "\\DATAA.txt";
    File archivo = new File(ruta);
    
    if (!archivo.exists()) {
        JOptionPane.showMessageDialog(null, "El archivo de datos no existe.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    boolean encontrado = false;
    
    try (BufferedReader lector = new BufferedReader(new FileReader(archivo))) {
        String linea;
        while ((linea = lector.readLine()) != null) {
            String[] datos = linea.split(",");
            if ((nombre.isEmpty() || datos[0].equalsIgnoreCase(nombre)) &&
                (telefono.isEmpty() || datos[1].equalsIgnoreCase(telefono)) &&
                (email.isEmpty() || datos[2].equalsIgnoreCase(email))) {
                
                jTextField1.setText(datos[0]);
                jTextField2.setText(datos[1]);
                jTextField3.setText(datos[2]);
                
                encontrado = true;
                break;
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    
    if (encontrado) {
        JOptionPane.showMessageDialog(null, "Registro encontrado y mostrado en los campos.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "No se encontró el registro.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton4ActionPerformed

