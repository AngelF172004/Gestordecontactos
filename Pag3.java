    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String nombre = jTextField1.getText().trim();
        String telefono = jTextField2.getText().trim();
        String email = jTextField3.getText().trim();

        // Depuración: Verificar si user tiene valor
        //System.out.println("Valor de user: " + dato);

        if (dato == null || dato.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El nombre de usuario no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (nombre.isEmpty() || telefono.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear la ruta del archivo
        //String user2= dato;
        String ruta = "C:\\FICHERO\\"+dato+"\\DATAA.txt";

       
        try {
            // Define el archivo "DATA.txt" en el directorio deseado
            //File archivo = new File("C:\\FICHERO\\prueba3\\DATAA.txt");
             File archivo = new File(ruta);
            
            
            // Crea el archivo si no existe
            if (!archivo.exists()) {
                archivo.getParentFile().mkdirs(); // Crea los directorios si no existen
                archivo.createNewFile();
            }

            // Abre el archivo en modo de escritura al final del archivo (append=true)
            FileWriter crear = new FileWriter(archivo, true);
            PrintWriter escribir = new PrintWriter(crear);

            // Escribe el usuario y la contraseña separados por una coma
            escribir.println(nombre + "," + telefono + "," + email);

            // Cierra los recursos
            escribir.close();
            crear.close();

            JOptionPane.showMessageDialog(null, "Registro guardado correctamente en el archivo DATA.txt y carpeta creada.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace(); // Muestra el error en la consola
        }
        
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

